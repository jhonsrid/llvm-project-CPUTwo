MCRegister CPUTwoAsmParser::tryParseRegName(StringRef Name) {
  MCRegister Reg = MatchRegisterName(Name.lower());
  if (Reg)
    return Reg;
  return MatchRegisterAltName(Name.lower());
}

bool CPUTwoAsmParser::parseRegister(MCRegister &Reg, SMLoc &StartLoc,
                                     SMLoc &EndLoc) {
  auto Status = tryParseRegister(Reg, StartLoc, EndLoc);
  return !Status.isSuccess();
}

ParseStatus CPUTwoAsmParser::tryParseRegister(MCRegister &Reg, SMLoc &StartLoc,
                                               SMLoc &EndLoc) {
  const AsmToken &Tok = Parser.getTok();
  StartLoc = Tok.getLoc();
  EndLoc = Tok.getEndLoc();

  if (Tok.isNot(AsmToken::Identifier))
    return ParseStatus::NoMatch;

  Reg = tryParseRegName(Tok.getString());
  if (!Reg)
    return ParseStatus::NoMatch;

  Parser.Lex(); // Eat register token
  return ParseStatus::Success;
}

bool CPUTwoAsmParser::parseOperand(OperandVector &Operands,
                                    StringRef Mnemonic) {
  // Try register
  MCRegister Reg;
  SMLoc S = Parser.getTok().getLoc();
  SMLoc E = Parser.getTok().getEndLoc();

  if (Parser.getTok().is(AsmToken::Identifier)) {
    Reg = tryParseRegName(Parser.getTok().getString());
    if (Reg) {
      E = Parser.getTok().getEndLoc();
      Parser.Lex();
      Operands.push_back(CPUTwoOperand::createReg(Reg, S, E));
      return false;
    }
  }

  // Try memory operand: [reg + imm] or [reg + reg] or [reg]
  if (Parser.getTok().is(AsmToken::LBrac)) {
    Parser.Lex(); // Eat '['
    S = Parser.getTok().getLoc();

    if (Parser.getTok().isNot(AsmToken::Identifier))
      return Error(S, "expected register");

    Reg = tryParseRegName(Parser.getTok().getString());
    if (!Reg)
      return Error(S, "expected register");

    Parser.Lex(); // Eat reg

    if (Parser.getTok().is(AsmToken::RBrac)) {
      // [reg] — base only, zero offset
      E = Parser.getTok().getEndLoc();
      Parser.Lex(); // Eat ']'
      Operands.push_back(CPUTwoOperand::createReg(Reg, S, E));
      Operands.push_back(CPUTwoOperand::createImm(
          MCConstantExpr::create(0, getContext()), S, E));
      return false;
    }

    if (Parser.getTok().isNot(AsmToken::Plus))
      return Error(Parser.getTok().getLoc(), "expected '+' or ']'");

    Parser.Lex(); // Eat '+'

    // Try reg + reg
    if (Parser.getTok().is(AsmToken::Identifier)) {
      MCRegister Reg2 = tryParseRegName(Parser.getTok().getString());
      if (Reg2) {
        Parser.Lex(); // Eat reg2
        if (Parser.getTok().isNot(AsmToken::RBrac))
          return Error(Parser.getTok().getLoc(), "expected ']'");
        E = Parser.getTok().getEndLoc();
        Parser.Lex(); // Eat ']'
        Operands.push_back(CPUTwoOperand::createReg(Reg, S, E));
        Operands.push_back(CPUTwoOperand::createReg(Reg2, S, E));
        return false;
      }
    }

    // reg + imm
    const MCExpr *Expr;
    if (getParser().parseExpression(Expr))
      return true;

    if (Parser.getTok().isNot(AsmToken::RBrac))
      return Error(Parser.getTok().getLoc(), "expected ']'");
    E = Parser.getTok().getEndLoc();
    Parser.Lex(); // Eat ']'

    Operands.push_back(CPUTwoOperand::createReg(Reg, S, E));
    Operands.push_back(CPUTwoOperand::createImm(Expr, S, E));
    return false;
  }

  // Try immediate/expression
  const MCExpr *Expr;
  S = Parser.getTok().getLoc();
  if (getParser().parseExpression(Expr))
    return true;
  E = SMLoc::getFromPointer(S.getPointer() - 1);
  Operands.push_back(CPUTwoOperand::createImm(Expr, S, E));
  return false;
}

bool CPUTwoAsmParser::parseInstruction(ParseInstructionInfo &Info,
                                        StringRef Name, SMLoc NameLoc,
                                        OperandVector &Operands) {
  // Add mnemonic as first operand
  Operands.push_back(CPUTwoOperand::createToken(Name, NameLoc));

  // If no operands, done
  if (Parser.getTok().is(AsmToken::EndOfStatement))
    return false;

  // Parse first operand
  if (parseOperand(Operands, Name))
    return true;

  // Parse remaining operands
  while (Parser.getTok().is(AsmToken::Comma)) {
    Parser.Lex(); // Eat ','
    if (parseOperand(Operands, Name))
      return true;
  }

  if (Parser.getTok().isNot(AsmToken::EndOfStatement))
    return Error(Parser.getTok().getLoc(), "unexpected token");

  return false;
}

bool CPUTwoAsmParser::matchAndEmitInstruction(SMLoc IDLoc, unsigned &Opcode,
                                               OperandVector &Operands,
                                               MCStreamer &Out,
                                               uint64_t &ErrorInfo,
                                               bool MatchingInlineAsm) {
  MCInst Inst;
  switch (MatchInstructionImpl(Operands, Inst, ErrorInfo, MatchingInlineAsm)) {
  case Match_Success:
    Inst.setLoc(IDLoc);
    Out.emitInstruction(Inst, getSTI());
    return false;
  case Match_MnemonicFail:
    return Error(IDLoc, "unrecognized instruction mnemonic");
  case Match_InvalidOperand: {
    SMLoc ErrorLoc = IDLoc;
    if (ErrorInfo != ~0ULL && ErrorInfo < Operands.size())
      ErrorLoc = Operands[ErrorInfo]->getStartLoc();
    return Error(ErrorLoc, "invalid operand for instruction");
  }
  case Match_MissingFeature:
    return Error(IDLoc, "instruction requires a feature not currently enabled");
  default:
    return true;
  }
}

unsigned CPUTwoAsmParser::validateTargetOperandClass(MCParsedAsmOperand &AsmOp,
                                                      unsigned Kind) {
  return Match_InvalidOperand;
}

