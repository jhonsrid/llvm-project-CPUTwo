struct CPUTwoOperand : public MCParsedAsmOperand {
  enum KindTy { Token, Register, Immediate } Kind;

  SMLoc StartLoc, EndLoc;

  union {
    StringRef Tok;
    MCRegister Reg;
    const MCExpr *Imm;
  };

  CPUTwoOperand(KindTy K, SMLoc S, SMLoc E) : Kind(K), StartLoc(S), EndLoc(E) {}

  static std::unique_ptr<CPUTwoOperand> createToken(StringRef Str, SMLoc S) {
    auto Op = std::make_unique<CPUTwoOperand>(Token, S, S);
    Op->Tok = Str;
    return Op;
  }

  static std::unique_ptr<CPUTwoOperand> createReg(MCRegister R, SMLoc S,
                                                    SMLoc E) {
    auto Op = std::make_unique<CPUTwoOperand>(Register, S, E);
    Op->Reg = R;
    return Op;
  }

  static std::unique_ptr<CPUTwoOperand> createImm(const MCExpr *Val, SMLoc S,
                                                    SMLoc E) {
    auto Op = std::make_unique<CPUTwoOperand>(Immediate, S, E);
    Op->Imm = Val;
    return Op;
  }

  // MCParsedAsmOperand interface
  SMLoc getStartLoc() const override { return StartLoc; }
  SMLoc getEndLoc() const override { return EndLoc; }

  bool isToken() const override { return Kind == Token; }
  bool isImm() const override { return Kind == Immediate; }
  bool isReg() const override { return Kind == Register; }
  bool isMem() const override { return false; }

  StringRef getToken() const {
    assert(Kind == Token);
    return Tok;
  }

  MCRegister getReg() const override {
    assert(Kind == Register);
    return Reg;
  }

  void addRegOperands(MCInst &Inst, unsigned N) const {
    assert(N == 1);
    Inst.addOperand(MCOperand::createReg(getReg()));
  }

  void addImmOperands(MCInst &Inst, unsigned N) const {
    assert(N == 1);
    if (auto *CE = dyn_cast<MCConstantExpr>(Imm))
      Inst.addOperand(MCOperand::createImm(CE->getValue()));
    else
      Inst.addOperand(MCOperand::createExpr(Imm));
  }

  void print(raw_ostream &OS, const MCAsmInfo &MAI) const override {
    switch (Kind) {
    case Token:
      OS << "Token: " << Tok;
      break;
    case Register:
      OS << "Reg: " << Reg;
      break;
    case Immediate:
      OS << "Imm";
      break;
    }
  }

  bool isSimm16() const {
    if (!isImm()) return false;
    if (auto *CE = dyn_cast<MCConstantExpr>(Imm))
      return isInt<16>(CE->getValue());
    return true; // Expressions could be anything
  }

  bool isUimm16() const {
    if (!isImm()) return false;
    if (auto *CE = dyn_cast<MCConstantExpr>(Imm))
      return isUInt<16>(CE->getValue());
    return true;
  }

  bool isShamt5() const {
    if (!isImm()) return false;
    if (auto *CE = dyn_cast<MCConstantExpr>(Imm))
      return isUInt<5>(CE->getValue());
    return false;
  }

  bool isBrtarget20() const { return isImm(); }
  bool isCCOp() const { return isImm(); }

  void addSimm16Operands(MCInst &Inst, unsigned N) const { addImmOperands(Inst, N); }
  void addUimm16Operands(MCInst &Inst, unsigned N) const { addImmOperands(Inst, N); }
  void addShamt5Operands(MCInst &Inst, unsigned N) const { addImmOperands(Inst, N); }
  void addBrtarget20Operands(MCInst &Inst, unsigned N) const { addImmOperands(Inst, N); }
  void addCCOpOperands(MCInst &Inst, unsigned N) const { addImmOperands(Inst, N); }
};

