void CPUTwoDAGToDAGISel::Select(SDNode *Node) {
  if (Node->isMachineOpcode()) {
    Node->setNodeId(-1);
    return;
  }

  SDLoc DL(Node);

  switch (Node->getOpcode()) {
  case CPUTwoISD::CALL: {
    // Convert CALL to JMP (direct) or CALLR (indirect)
    SDValue Callee = Node->getOperand(1);
    unsigned CallOpc;
    if (Callee.getOpcode() == ISD::TargetGlobalAddress ||
        Callee.getOpcode() == ISD::TargetExternalSymbol)
      CallOpc = CPUTwo::JMP;
    else
      CallOpc = CPUTwo::CALLR;

    // Machine nodes need chain and glue at end
    SmallVector<SDValue, 8> Ops;
    // Explicit operands (skip chain at 0, skip glue at end)
    unsigned NumOps = Node->getNumOperands();
    bool HasGlue = Node->getOperand(NumOps - 1).getValueType() == MVT::Glue;
    unsigned LastExplicit = HasGlue ? NumOps - 1 : NumOps;
    for (unsigned i = 1; i < LastExplicit; ++i)
      Ops.push_back(Node->getOperand(i));
    // Chain
    Ops.push_back(Node->getOperand(0));
    // Glue
    if (HasGlue)
      Ops.push_back(Node->getOperand(NumOps - 1));

    CurDAG->SelectNodeTo(Node, CallOpc, Node->getVTList(), Ops);
    return;
  }
  case CPUTwoISD::HI16: {
    CurDAG->SelectNodeTo(Node, CPUTwo::LUI, MVT::i32, Node->getOperand(0));
    return;
  }
  case CPUTwoISD::LO16: {
    CurDAG->SelectNodeTo(Node, CPUTwo::ORI, MVT::i32, Node->getOperand(0),
                         Node->getOperand(1));
    return;
  }
  default:
    break;
  }

  SelectCode(Node);
}

void CPUTwoDAGToDAGISel::selectCall(SDNode *Node) {
  SDLoc DL(Node);
  SDValue Callee = Node->getOperand(1);

  unsigned CallOpc;
  if (isa<GlobalAddressSDNode>(Callee) ||
      isa<ExternalSymbolSDNode>(Callee)) {
    CallOpc = CPUTwo::JMP;
  } else {
    CallOpc = CPUTwo::CALLR;
  }

  // Use SelectNodeTo to convert in-place, preserving the chain/glue properly
  SmallVector<SDValue, 8> Ops;

  // For JMP, convert callee to target form
  if (auto *GA = dyn_cast<GlobalAddressSDNode>(Callee)) {
    Ops.push_back(CurDAG->getTargetGlobalAddress(GA->getGlobal(), DL,
                                                  MVT::i32, GA->getOffset()));
  } else if (auto *ES = dyn_cast<ExternalSymbolSDNode>(Callee)) {
    Ops.push_back(CurDAG->getTargetExternalSymbol(ES->getSymbol(), MVT::i32));
  } else {
    Ops.push_back(Callee);
  }

  // Remaining operands (register mask, register operands, glue)
  for (unsigned i = 2; i < Node->getNumOperands(); ++i)
    Ops.push_back(Node->getOperand(i));

  // Add chain
  Ops.push_back(Node->getOperand(0));

  CurDAG->SelectNodeTo(Node, CallOpc, Node->getVTList(), Ops);
}

FunctionPass *llvm::createCPUTwoISelDag(CPUTwoTargetMachine &TM) {
  return new CPUTwoDAGToDAGISelLegacy(TM, TM.getOptLevel());
}
