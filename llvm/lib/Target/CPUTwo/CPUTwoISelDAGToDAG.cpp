#include "CPUTwo.h"
#include "CPUTwoSubtarget.h"
#include "CPUTwoTargetMachine.h"
#include "llvm/CodeGen/MachineFunction.h"
#include "llvm/CodeGen/SelectionDAGISel.h"

using namespace llvm;

#define DEBUG_TYPE "cputwo-isel"

namespace {

class CPUTwoDAGToDAGISel : public SelectionDAGISel {
public:
  CPUTwoDAGToDAGISel() = delete;

  explicit CPUTwoDAGToDAGISel(CPUTwoTargetMachine &TM, CodeGenOptLevel OL)
      : SelectionDAGISel(TM, OL) {}

  void Select(SDNode *Node) override;

  // Include the pieces autogenerated from the target description.
#include "CPUTwoGenDAGISel.inc"
};

class CPUTwoDAGToDAGISelLegacy : public SelectionDAGISelLegacy {
public:
  static char ID;

  explicit CPUTwoDAGToDAGISelLegacy(CPUTwoTargetMachine &TM,
                                     CodeGenOptLevel OL)
      : SelectionDAGISelLegacy(ID, std::make_unique<CPUTwoDAGToDAGISel>(TM, OL)) {}
};

char CPUTwoDAGToDAGISelLegacy::ID = 0;

} // end anonymous namespace

void CPUTwoDAGToDAGISel::Select(SDNode *Node) {
  // If we have a custom node, we already have selected
  if (Node->isMachineOpcode()) {
    Node->setNodeId(-1);
    return;
  }

  // Select the default instruction
  SelectCode(Node);
}

FunctionPass *llvm::createCPUTwoISelDag(CPUTwoTargetMachine &TM) {
  return new CPUTwoDAGToDAGISelLegacy(TM, TM.getOptLevel());
}
